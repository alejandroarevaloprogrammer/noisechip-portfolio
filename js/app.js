function setCategory(category, button) {
  currentCategory = category;

  document
    .querySelectorAll(".filter-btn")
    .forEach(btn => btn.classList.remove("active"));

  if (button) {
    button.classList.add("active");
  }

  filteredArtworks =
    category === "all"
      ? artworks
      : artworks.filter(a => a.category === category);

  renderGallery();
}


// =====================================
// GALLERY
// =====================================

function renderGallery() {

  const g = document.getElementById("gallery");
  g.innerHTML = "";

  visibleArtworks = getOrderedList();

  visibleArtworks.forEach((item, i) => {

    const showBadge =
      currentCategory === "all" &&
      categoryLabels[item.category];

    const badge = showBadge
      ? `<div class="category-badge">${categoryLabels[item.category]}</div>`
      : "";

    g.innerHTML += `
      <div class="col-md-6 col-lg-4">
        <div class="card position-relative ${item.category === "projects" ? "project-card" : ""}"
             onclick="openModal(${i})">

          ${badge}

          <img src="${item.image}" class="card-img-top" alt="${item.title}">

          <div class="art-overlay">
            <div class="art-title">${item.title}</div>
            ${item.desc ? `<div class="art-desc">${item.desc}</div>` : ""}
          </div>

        </div>
      </div>
    `;

  });

  adjustImageSizes();
}


// =====================================
// IMAGE SIZE ADJUSTMENTS
// =====================================

function adjustImageSizes() {
  document.querySelectorAll("#gallery img").forEach(img => {

    const apply = () => {

      if (
        img.naturalWidth === 800 &&
        img.naturalHeight === 800
      ) {
        const col = img.closest(".col-md-6, .col-lg-4");

        if (col) {
          col.classList.remove("col-lg-4");
          col.classList.add("col-md-6");
        }

        img.style.height = "380px";
        img.style.objectFit = "contain";
      }
    };

    if (img.complete) {
      apply();
    } else {
      img.onload = apply;
    }

  });
}


