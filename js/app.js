function setMainImage(src) {

  const topViewer =
    document.getElementById("modalImage");

  topViewer.style.display = "none";

  const clicked = event.target;

  const section =
    clicked.closest(".project-gallery");

  if (!section) return;

  sectionImages = Array.from(
    section.querySelectorAll("img")
  ).map(img => img.src);

  const fullSrc =
    new URL(src, window.location.href).href;

  currentDynamicIndex =
    sectionImages.indexOf(fullSrc);

  if (currentDynamicIndex < 0) {
    currentDynamicIndex = 0;
  }

  renderDynamicViewer(section);
}


function renderDynamicViewer(section) {

  currentSection = section;

  document
    .querySelectorAll(".dynamic-viewer")
    .forEach(el => el.remove());

  const viewer =
    document.createElement("div");

  viewer.className = "dynamic-viewer";

  viewer.innerHTML = `
    <button class="viewer-arrow left"
      onclick="moveDynamic(-1)">❮</button>

    <img
      src="${sectionImages[currentDynamicIndex]}"
      class="dynamic-viewer-img"
    >

    <button class="viewer-arrow right"
      onclick="moveDynamic(1)">❯</button>
  `;

  section.parentNode.insertBefore(
    viewer,
    section
  );

  // remove active state from ALL thumbnails
  document
    .querySelectorAll(".project-gallery img")
    .forEach(img => {
      img.classList.remove("active-thumb");
    });

  // activate only current section thumb
  const thumbs =
    currentSection.querySelectorAll("img");

  if (thumbs[currentDynamicIndex]) {
    thumbs[currentDynamicIndex]
      .classList.add("active-thumb");
  }

  setTimeout(() => {
    viewer.scrollIntoView({
      behavior: "smooth",
      block: "nearest"
    });
  }, 50);
}


function moveDynamic(step) {

  currentDynamicIndex += step;

  if (currentDynamicIndex < 0) {
    currentDynamicIndex = sectionImages.length - 1;
  }

  if (currentDynamicIndex >= sectionImages.length) {
    currentDynamicIndex = 0;
  }

  renderDynamicViewer(currentSection);

  const thumbs =
    currentSection.querySelectorAll("img");

  thumbs.forEach(img =>
    img.classList.remove("active-thumb")
  );

  if (thumbs[currentDynamicIndex]) {
    thumbs[currentDynamicIndex].classList.add("active-thumb");
  }
}


