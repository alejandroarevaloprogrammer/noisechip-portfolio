function nextImage() {
  currentIndex =
    (currentIndex + 1) %
    visibleArtworks.length;

  updateModal();
}

function prevImage() {
  currentIndex =
    (currentIndex - 1 +
      visibleArtworks.length) %
    visibleArtworks.length;

  updateModal();
}

