function openModal(i) {
  currentIndex = i;

  const modalImage =
    document.getElementById("modalImage");

  modalImage.style.display = "block";

  document
    .querySelectorAll(".dynamic-viewer")
    .forEach(el => el.remove());

  updateModal();

  new bootstrap.Modal(
    document.getElementById("imageModal")
  ).show();
}


// =====================================
// MODAL UPDATE
// =====================================

function updateModal() {
  const item = visibleArtworks[currentIndex];

  const modalImage =
    document.getElementById("modalImage");

  const title =
    document.getElementById("modalTitle");

  const desc =
    document.getElementById("modalDesc");

  const gallery =
    document.getElementById("projectSections");

  const bottomArrows =
    document.querySelector(".bottom-arrows");

  const projectNav =
    document.querySelector(".project-nav");

  const category =
    document.getElementById("modalCategory");

  // reset dynamic viewers
  document
    .querySelectorAll(".dynamic-viewer")
    .forEach(el => el.remove());

  modalImage.src = item.image;
  modalImage.alt = item.title;
  title.innerText = item.title;

  if (item.category === "projects") {

    category.innerText = "";
    desc.style.marginTop = "0";

    document
      .querySelectorAll(".nav-arrow")
      .forEach(el => el.style.display = "none");

    if (bottomArrows) {
      bottomArrows.style.display = "none";
    }

    if (projectNav) {
      projectNav.style.display = "flex";
    }

    modalImage.style.display = "none";

    renderProjectContent(item, gallery, desc);

  } else {

    if (currentCategory === "all") {
      category.innerText =
        categoryLabels[item.category] || "";
    } else {
      category.innerText = "";
    }

    document
      .querySelectorAll(".nav-arrow")
      .forEach(el => el.style.display = "none");

    if (bottomArrows) {
      bottomArrows.style.display = "flex";
    }

    if (projectNav) {
      projectNav.style.display = "none";
    }

    modalImage.style.display = "block";

    desc.innerText = item.desc || "";
    desc.style.marginTop = "4px";

    if (gallery) {
      gallery.innerHTML = "";
    }
  }
}



